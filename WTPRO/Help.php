    <span><h1>Instruction to Movie viewers</h1></span>
    <p>
        &emsp;&emsp;&emsp;<p>To book tickets for a movie at a cinema hall, you can follow these steps:</p>

<h3>1. **Visit the Cinema's Website or Mobile App**:</h3>
&emsp;&emsp;&emsp;- Open your web browser or mobile app store.<br>
&emsp;&emsp;&emsp;- Search for the official website or mobile app of the cinema hall you want to visit.<br>

<h3>2. **Create an Account (If Required)**:</h3>
&emsp;&emsp;&emsp;- If it's your first time booking tickets online with this cinema, you may need to create an account. This typically involves providing your email address, creating a password, and sometimes adding personal details.<br>

<h3>3. **Select Location and Movie**:</h3>
&emsp;&emsp;&emsp;- Once you're logged in, select your location or the specific cinema hall you want to visit.<br>

<h3>4. **Browse Movie Listings**:</h3>
&emsp;&emsp;&emsp;- Look for the movie you want to watch and click on it to view showtimes.<br>

<h3>5. **Select Showtime and Seats**:</h3>
&emsp;&emsp;&emsp;- Choose the date and time you want to watch the movie. This will display the available seats for that particular showtime.<br>

<h3>6. **Choose Seats**:</h3>
&emsp;&emsp;&emsp;- Click on the seats you want to reserve. They are usually marked as available with a different color. Some platforms allow you to select specific seats, while others might assign the best available seats automatically.<br>

<h3>7. **Add to Cart and Review**:</h3>
&emsp;&emsp;&emsp;- After selecting your seats, add them to your cart. Review your selection to make sure everything is correct.

<h3>8. **Provide Additional Details**:</h3>
&emsp;&emsp;&emsp;- Depending on the cinema, you may need to provide additional details like the number of tickets, ticket types (e.g., adult, child, senior), and any special requests.<br>

<h3>9. **Apply Promo Codes or Discounts (If Available)**:</h3>
&emsp;&emsp;&emsp;- If you have any promo codes or discounts, apply them at this stage.

<h3>10. **Payment**:</h3>
&emsp;&emsp;&emsp;- Enter your payment information to complete the transaction. Most cinemas accept credit/debit cards, and some may offer other payment methods like digital wallets.<br>

<h3>11. **Confirmation**:</h3>
&emsp;&emsp;&emsp;- After successful payment, you should receive a confirmation email or a booking code. This will serve as your ticket.<br>

<h3>12. **Collecting Tickets**:</h3>
&emsp;&emsp;&emsp;- Depending on the cinema's policy, you may need to collect physical tickets at the cinema's box office or use an e-ticket on your mobile device.<br><br>

&emsp;&emsp;&emsp;Remember, it's always a good idea to arrive a bit early, especially on busy days, to allow time for parking, concessions, and finding your seats.<br><br>

&emsp;&emsp;&emsp;If you encounter any issues during the booking process, most cinemas have customer support options available through their website or app.
